    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        float pwr,spd,intrvl;
        double grd;
        try{
            pwr = Float.parseFloat(jTextField2.getText());
            grd = Double.parseDouble(jTextField2.getText());
            intrvl = Float.parseFloat(jTextField4.getText());
        }catch(NumberFormatException e){
            pwr = 0;
            grd = 0;
            intrvl = 0;
        }
        TM.setPower(pwr);
        TM.setGradient(grd);
        TM.updatePhyicsState(intrvl);
        spd = TM.getSpeed();
        jLabel8.setText(String.valueOf(spd));
        jLabel15.setText(String.valueOf(TM.getDistanceTravelled()));
        jTextField2.setText(String.valueOf(TM.getPower()));
    }//GEN-LAST:event_jButton2ActionPerformed

